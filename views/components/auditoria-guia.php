    <div class="auditoria-guia-body">
        <p class="auditoria-guia-intro">
            La auditoría es el <strong>historial de seguridad</strong> del sistema. Cada vez que alguien crea, modifica,
            elimina o exporta información importante, queda registrado <em>quién lo hizo</em>, <em>cuándo</em>,
            <em>en qué módulo</em> y <em>qué cambió</em>.
        </p>
        <div class="auditoria-guia-grid">
            <article class="auditoria-guia-item">
                <span class="badge badge-success">Registro nuevo</span>
                <p>Alguien dio de alta un vehículo, comisión, usuario, carga de combustible, etc.</p>
            </article>
            <article class="auditoria-guia-item">
                <span class="badge badge-warning">Modificación</span>
                <p>Se actualizó un dato existente: estado de comisión, datos de vehículo, alerta atendida, etc.</p>
            </article>
            <article class="auditoria-guia-item">
                <span class="badge badge-danger">Eliminación</span>
                <p>Se quitó información del sistema, por ejemplo una foto de evidencia de daño.</p>
            </article>
            <article class="auditoria-guia-item">
                <span class="badge badge-info">Exportación</span>
                <p>Alguien descargó un reporte o generó un formato PDF desde el sistema.</p>
            </article>
        </div>
        <ul class="auditoria-guia-list">
            <li><strong>Módulo:</strong> la sección del sistema donde ocurrió (Vehículos, Comisiones, Usuarios…).</li>
            <li><strong>Qué pasó:</strong> resumen en lenguaje sencillo del cambio realizado.</li>
            <li><strong>Referencia:</strong> número interno del registro afectado; útil si necesita rastrear un caso concreto.</li>
            <li><strong>Acceso:</strong> solo el rol <em>Administrador General</em> puede consultar esta pantalla.</li>
        </ul>
